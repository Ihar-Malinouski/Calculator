package pages;

public class  CalculatorPage {

    public int additionOfNumbers(int a, int b) {
        return a + b;
    }

    public int subtractionOfNumbers(int a, int b) {
        return a - b;
    }

    public int multiplicationOfNumbers(int a, int b) {
        return a * b;
    }

    public int divisionOfNumbers(int a, int b) {
        return a / b;
    }
}